# Condições verdadeiras (true) ou falsas (false)

# Condicionais
if True:
    print("Bloco executado")
else:
    print("Bloco não executado")

idade = 23
if idade >= 18:
    print("Maior de idade")
else:
    print("Menor de idade")

# Operadores lógicos (and e or)
if True and True:
    print("And - ambas variáveis devem ser verdadeiras")
if True or False:
    print("Or - uma das variáveis deve ser verdadeira")