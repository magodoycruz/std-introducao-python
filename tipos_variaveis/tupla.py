# Tupla - Coleção ordenada e imutável de elementos

minha_tupla = (1, 2, 2, 3, 4)
print(minha_tupla)

print(minha_tupla[2])
print(minha_tupla[-1]) # Ultimo elemento de uma lista ou tupla

# Quantidade de vezes que um elemento aparece na tupla - count()
contagem = minha_tupla.count(2)
print(contagem)

# Posição da primeira vez que um elemento aparece na tupla - index()
indice = minha_tupla.index(2)
print(indice)